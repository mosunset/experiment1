// Thesis 博士論文/修士論文のデータ
public class Thesis {
    String authors;
    String title;
    String degree;
    String school;
    int year;

    public Thesis(String authors, String title, String degree, String school, int year) {
        this.authors = authors;
        this.title = title;
        this.degree = degree;
        this.school = school;
        this.year = year;
    }
}
